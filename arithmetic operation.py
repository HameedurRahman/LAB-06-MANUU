
a=int(input("Enter first number="))
b=int(input("Enter second number="))

print("The sum of {0} and {1} is {2}".format(a,b,a+b))
print("The difference between {0} and {1} is {2}".format(a,b,a-b))
print("The multiplication of {0} and {1} is {2}".format(a,b,a*b))
print("The division of {0} and {1} is {2}".format(a,b,a/b))
print("The modulus of {0} and {1} is {2}".format(a,b,a%b))
print("The exponent terms of {0} and {1} is {2}".format(a,b,a**b))
print("The floor division of {0} and {1} is {2}".format(a,b,a//b))
